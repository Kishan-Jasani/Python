# Data Structure:-
# --> Data structure is a data organization, management, and storage format that enables efficient access and modification. 
# --> More precisely, Data structure is a collection of data values, the relationships among them, and the functions or operations that can be applied to the data.

# --> Data structures represent the way of arranging data in computer so that it can be accessed and used efficiently. 
# --> It is all about grouping and storing collections of data in memory, operations that can be performed on these collections and algorithms that can be used for these operations.
# --> There are different types of data structures like list, stack, queue etc. Each of them are suitable for specific type of tasks.


